import type { Metadata } from "next";
import { Geist, Geist_Mono } from "next/font/google";
import "./globals.css";

const geistSans = Geist({
  variable: "--font-geist-sans",
  subsets: ["latin"],
});

const geistMono = Geist_Mono({
  variable: "--font-geist-mono",
  subsets: ["latin"],
});

export const metadata: Metadata = {
  title: "Signalist",
  description: "Real-time stock price tracking with personalized alerts and detailed company insights for smarter investing. ",
};

/**
 * Root layout component that renders the top-level HTML and body wrappers,
 * applies global font CSS variables and the dark theme, and renders page content.
 *
 * @param children - The React nodes to be rendered inside the document body
 * @returns The top-level HTML element containing the application body and children
 */
export default function RootLayout({
  children,
}: Readonly<{
  children: React.ReactNode;
}>) {
  return (
    <html lang="en" className="dark">
      <body
        className={`${geistSans.variable} ${geistMono.variable} antialiased`}
      >
        {children}
      </body>
    </html>
  );
}